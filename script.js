// --- 1. Music Logic (Local Folder Playlist) ---
const audio = document.getElementById("bgMusic");
const btn = document.getElementById("musicBtn");

// Yahan folder wale gano ke naam likhein
const playlist = [
    "song1.mp3",
    "song2.mp3",
    "song3.mp3",
    "song4.mp3" 
];

let currentSongIndex = 0;

// Play/Pause Function
function toggleMusic() {
    if (!audio.src || audio.src === window.location.href) {
        changeSong(0); 
    } else {
        if (audio.paused) { 
            audio.play().catch(e => alert("Please interact with the page first.")); 
            btn.innerText = "â¸ Pause"; 
        } else { 
            audio.pause(); 
            btn.innerText = "ðŸŽµ Play"; 
        }
    }
}

// Next Song Function
function playNextSong() {
    let nextIndex = Math.floor(Math.random() * playlist.length);
    if (nextIndex === currentSongIndex) {
        nextIndex = (currentSongIndex + 1) % playlist.length;
    }
    changeSong(nextIndex);
}

// Change Song Function
function changeSong(index) {
    currentSongIndex = index;
    audio.src = playlist[currentSongIndex];
    audio.load();
    audio.play().then(() => {
        btn.innerText = "â¸ Pause";
    }).catch(e => {
        console.log("Error playing: " + playlist[index]);
    });
}

// Auto-play next
audio.onended = function() {
    playNextSong();
};

